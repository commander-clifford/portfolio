		<p>
			Check out these bicycle icons. Each icon represents a riding style and is shaped accordingly.
			These icons were for a side project, it would be like Yelp for bicycle trails.
			These icons would represent the riding style for each trail / park.
		</p>
		<p>
			I started by abstracting the shape of my full suspension bike (the All Mountain (AM)),
			then adjusting lines and points accordingly to get the shapes of the other bikes.
			I'm somewhat satisfied with the shapes of each bike, as they fairly accurately
			depict each bicycle style. However, when these get re-built, I'll be looking at
			alternative typography solutions and probably ditch the 'app icon' feel.
			I'll also likely use SVG outputs and animate pieces:
			spinning wheels/pedals, compressing suspension components, etc.
		</p>
		<p>
			I've always believed in using the correct image technology for the given image.
			For a portrait or a landscape, fine, use a JPEG or maybe PNG.
			But when it comes to icons and logos, I lean towards using SVG or glyphicons.
			They can be so much more flexible than a bitmap image. SVG images
			for example are very flexible and can be animated.
		</p>
